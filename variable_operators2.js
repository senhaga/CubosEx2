let x = 10;

x += 5;
//Equivalent to x = x + 5
console.log(x);

x -= 5;
//Equivalent to x = x - 5
console.log(x);

x *= 5
//Equivalent to x = x * 5
console.log(x);

x /= 5;
//Equivalent to x = x / 5
console.log(x);

//------xxx------
x = x % 3; //modulo operator
console.log(x);

x++;
console.log(x);

x--;
console.log(x);

let z = x++; // z gets value of x before adding 1 to itself
console.log(x, z);

let y = ++x; // y gets value opf x after adding 1 to itself
console.log(x, y);








